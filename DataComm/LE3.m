AMP1 = 2; 
AMP2 = 10; 
FREQ1 = 14; 
FREQ2 = 26; 
fs=1000; 
P1 = 0; 
P2 = 30*pi/180; 
T1 = 1/FREQ1; 
T2 = 1/FREQ2; 
t1 = linspace(0,2*T1,1000); 
t2 = linspace(0,2*T2,1000); 
x1 = AMP1*sin(2*pi*FREQ1*t1+P1); 
x2 = AMP2*sin(2*pi*FREQ2*t2+P2); 
x=x1+x2; 
noise_amp = 0.2; 
x1_noisy = x1 + noise_amp*randn(1,length(x1)); 
x2_noisy = x2 + noise_amp*randn(1,length(x2)); 
signal_power = (sum(x1.^2)+sum(x2.^2))/1000; 
noise_power = noise_amp*95; 
Bandwidth=obw(x,fs) 
SNR = signal_power/noise_power 
Maxcapacity = Bandwidth*log2(1+SNR) 